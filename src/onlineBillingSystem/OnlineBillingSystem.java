    private void BequalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BequalActionPerformed
        if (TAcalculator.getText().equals("")) {

        } else {
            secondnum = Double.parseDouble(TAcalculator.getText());
            switch (operation) {
                case "%":
                    result = firstnum % secondnum;
                    break;
                case "/":
                    result = firstnum / secondnum;
                    break;
                case "*":
                    result = firstnum * secondnum;
                    break;
                case "-":
                    result = firstnum - secondnum;
                    break;
                case "+":
                    result = firstnum + secondnum;
                    break;
                default:
                    result = firstnum;
            }
            firstnum = result;
            answer = String.format("%.0f", result);
            TAcalculator.setText(answer);
            dotCount = false;
        }
    }//GEN-LAST:event_BequalActionPerformed

