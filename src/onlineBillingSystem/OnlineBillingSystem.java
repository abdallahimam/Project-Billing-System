    private void BcaculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BcaculateActionPerformed
        try {
            double l;
            double t;
            double p = Double.parseDouble(TAplastic.getText());
            double c = Double.parseDouble(TAcopper.getText());
            double ch = Double.parseDouble(TAcopper.getText());
            double ir = Double.parseDouble(TAiron.getText());
            double st = Double.parseDouble(TAsteal.getText());
            double r, v, i, m, x, total;
            r = v = i = m = x = total = 0.0;
            if (!CHBTax.isSelected() && !ChBVAT.isSelected()) {
                TASubTotal.setText("");
                TATAX.setText("");
                TATaxcheckBox.setText("");
                TATotalAmount.setText("");
                TAVAT.setText("");
                TAcalculator.setText("");
                TAchrome.setText("");
                TAcopper.setText("");
                TAlabour.setText("");
                TAdelivery.setText("");
                TAitems.setText("");
                TAmileage.setText("");
                TAplastic.setText("");
                TAiron.setText("");
                TAsteal.setText("");
                TAtravel.setText("");
            }

            if (ChBlabour.isSelected()) {
                if (!TAlabour.getText().isEmpty()) {
                    l = Double.parseDouble(TAlabour.getText());
                    r += (l * labour);
                    v += r * 0.05;
                    x += r * 0.15;
                    m += r * 0.10;
                    i += r * 0.01;
                    total += l;
                }
            }
            if (ChBtravel.isSelected()) {
                if (!TAtravel.getText().isEmpty()) {
                    t = Double.parseDouble(TAtravel.getText());
                    r += (t * travel);
                    v += r * 0.05;
                    x += r * 0.15;
                    m += r * 0.10;
                    i += r * 0.01;
                    total += t;
                }
            }
            if (ChBplastic.isSelected()) {
                if (!TAplastic.getText().isEmpty()) {
                    p = Double.parseDouble(TAplastic.getText());
                    r += (p * plastic);
                    v += r * 0.05;
                    x += r * 0.15;
                    m += r * 0.10;
                    i += r * 0.01;
                    total += p;
                }
            }
            if (ChBcopper.isSelected()) {
                if (!TAcopper.getText().isEmpty()) {
                    c = Double.parseDouble(TAcopper.getText());
                    r += (c * copper);
                    v += r * 0.05;
                    x += r * 0.15;
                    m += r * 0.10;
                    i += r * 0.01;
                    total += c;
                }
            }
            if (ChBchrome.isSelected()) {
                if (!TAchrome.getText().isEmpty()) {
                    ch = Double.parseDouble(TAchrome.getText());
                    r += (ch * chrome);
                    v += r * 0.05;
                    x += r * 0.15;
                    m += r * 0.10;
                    i += r * 0.01;
                    total += ch;
                }
            }
            if (ChBiron.isSelected()) {
                if (!TAiron.getText().isEmpty()) {
                    ir = Double.parseDouble(TAiron.getText());
                    r += (ir * iron);
                    v += r * 0.05;
                    x += r * 0.15;
                    m += r * 0.10;
                    i += r * 0.01;
                    total += ir;
                }
            }
            if (ChBstreal.isSelected()) {
                if (!TAsteal.getText().isEmpty()) {
                    st = Double.parseDouble(TAsteal.getText());
                    r += (st * steal);
                    v += r * 0.05;
                    x += r * 0.15;
                    m += r * 0.10;
                    i += r * 0.01;
                    total += st;
                }
            }
            if (total != 0.0) {
                String temp = String.format("%.0f", r);
                TAitems.setText(temp);
                temp = String.format("%.0f", i);
                TAdelivery.setText(temp);
                temp = String.format("%.0f", m);
                TAmileage.setText(temp);
                temp = String.format("%.0f", x);
                TATaxcheckBox.setText(temp);
                temp = String.format("%.0f", v);
                TAVAT.setText(temp);
                double subT = r + i + m;
                double taxT = v + x;
                temp = String.format("%.0f", subT);
                TASubTotal.setText(temp);
                temp = String.format("%.0f", taxT);
                TATAX.setText(temp);
                temp = String.format("%.0f", total);
                TATotalAmount.setText(temp);
            } else {

            }

        } catch (Exception e) {
        }
    }//GEN-LAST:event_BcaculateActionPerformed

